                                    <h2>Sobre o Exame</h2>
                                    <p>O Tilt-Test, também conhecido como teste da inclinação ou teste do estresse postural, é um exame complementar não invasivo utilizado na investigação de perda súbita e transitória da consciência , as chamadas sincopes ou desmaios. O exame pode ser realizado em pacientes de qualquer idade.
Para a realização do Tilt-Test não é necessária internação. É preciso apenas que o paciente esteja em jejum por 4 horas antes do procedimento. O exame é realizado por médicos especializados no serviço de eletrofisiologia de hospital ou clínica , e o paciente é liberado logo após o término da avaliação, aguardando-se apenas sua completa recuperação. É recomendado ficar de repouso e evitar de dirigir por no minimo 2 horas. </p>

