                                    <h2>Sobre o Exame</h2>
                                    <p>A ecocardiografia ou ecocardiograma bidimensional com doppler é um exame de ultrassom, no qual as imagens do coração, captadas por um transdutor colocado sobre o tórax do paciente, são transmitidas para um monitor. É um método diagnóstico muito utilizado em cardiologia para a detecção de alterações estruturais e/ou funcionais do coração.</p>
