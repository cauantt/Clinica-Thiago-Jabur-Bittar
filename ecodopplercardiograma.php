                                        <h5>Limitações do exame</h5>
                                    </div>
                                    <div class="acc-content">
                                        <div class="text">
                                            <p>Em pacientes que apresentem:</p>

                                            <p>Limitação de janela acústica por interposição de ar ou tecidos (enfisema subcutâneo, doença pulmonar obstrutiva periférica (DPOC), obesidade, prótese mamária, entre outras).</p>

                                            <p>Limitação de acesso ao tórax (curativos, feridas cirúrgicas, drenos, entre outros).</p>

                                            <p>A presença de líquidos (por exemplo: derrame pleural ou pericárdico) não costuma prejudicar a imagem devido ao meio líquido ser facilitador da transmissão do ultrassom.</p>


