                                        <div class="text">
                                            <p>A sensibilidade (chance de o exame ser positivo quando a doença arterial coronária está presente) e a especificidade (chance do exame ser negativo quando a doença arterial coronária está ausente) do Teste ergométrico situam-se entre 70% e 80 %.</p><p>

<p>Algumas vezes, o teste ergométrico pode ser “falso-positivo”, especialmente nas mulheres, sem significar doença arterial coronária obstrutiva.</p>

<p>O Teste ergométrico pode ser realizado em associação a outros procedimentos?</p>

<p>O Teste ergométrico pode ser associado à realização de exame de Medicina Nuclear (Cintilografia de Perfusão Miocárdica associada ao Teste Ergométrico) e ao Ecocardiograma (Ecocardiografia sob Estresse Físico)..</p>
