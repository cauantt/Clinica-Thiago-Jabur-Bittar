                                        <div class="text">
                                             <p>O primeiro passo é coletar dados com o paciente e descritos no pedido médico para definirmos a indicação do exame, afastar contraindicações e estabelecer o protocolo ideal de esforço para cada paciente.

<p>A seguir são colocados 10 eletrodos no tórax do paciente para o registro do eletrocardiograma. O paciente é colocado então na esteira rolante iniciando-se o exercício com o protocolo escolhido.

<p>A interrupção do exame ocorrerá caso o paciente apresente grande cansaço ou exaustão, sintomas indicativos de anormalidades cardiovasculares, alterações compatíveis com isquemia ou alterações significativas do ritmo cardíaco.

<p>Traçados eletrocardiográficos e medida da pressão arterial serão registrados antes do esforço, ao final de cada etapa do exercício e regularmente na recuperação.

<p>Quais os critérios utilizados para dizer que o teste foi POSITIVO para presença de isquemia?

<p>A interpretação do exame envolve múltiplos fatores: presença de sintomas, os níveis de pressão arterial, o comportamento da frequência cardíaca, a capacidade física, os distúrbios do ritmo cardíaco e as alterações do observadas no eletrocardiograma registrado durante o esforço.</p>
