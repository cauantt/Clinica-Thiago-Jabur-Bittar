                                    <h2>Sobre o Exame</h2>
                                    <p>O teste ergométrico serve para a avaliação ampla do funcionamento cardiovascular, quando submetido a esforço físico gradualmente crescente, em esteira rolante. São observados os sintomas, os comportamentos da frequência cardíaca, da pressão arterial e do eletrocardiograma antes, durante e após o esforço.</p><p>

Os principais objetivos do teste são diagnosticar e avaliar a doença arterial coronária. Avalia também a capacidade funcional cárdiorrespiratória; detecção de arritmias, de anormalidades da pressão arterial e de isquemia miocárdica; avaliar surgimento de sopros, sinais de falência ventricular esquerda e dos eventuais sintomas que podem acompanhar essas disfunções; avaliação funcional de doença cardíaca já conhecida; prescrição de exercícios físicos.</p>
