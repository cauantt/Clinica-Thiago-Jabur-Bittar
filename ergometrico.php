                                        <h5>Preparo</h5>
                                    </div>
                                    <div class="acc-content">
                                        <div class="text">
                                            <p>No dia do exame, após o banho, não utilizar cremes, pomadas ou gel;<br>

Vir ou trazer roupa confortável (agasalho/tênis).<br>

Não fumar 2 horas antes e 1 hora após o exame;<br>

Dieta normal 2 horas antes ou dieta leve 1 h antes do exame; o paciente não deve fazer o exame em jejum;<br>

A suspensão de medicação em uso fica a critério do seu médico e na dependência dos objetivos do exame. Quando o objetivo é diagnosticar doença arterial coronária, caso seja possível, recomenda-se suspender medicamentos que possam mascarar as manifestações da doença, como betabloqueadores (atenolol, propranolol, metoprolol etc), inibidores dos canais de cálcio (diltiazem, verapamil) e vasodilatadores coronarianos (dinitrato ou mononitrato de isossorbida, nitroglicerina etc).<br>

Recomenda-se ao paciente não expor o tórax desprotegido ao sol, até 72 horas após o exame, pois pode surgir irritação da pele, no local da colocação dos eletrodos.<br>

Obs.: Endoscopia digestiva ou Colonoscopia não devem ser realizadas no mesmo dia do teste ergométrico.</p>
